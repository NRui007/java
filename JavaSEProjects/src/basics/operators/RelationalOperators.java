package basics.operators;

public class RelationalOperators {
    public static void main(String[] args) {
        int a = 10;
        int b = 3;
        int c = 5;

        System.out.println("a > b is " + (a > b));
        System.out.println("a < b is " + (a < b));
        System.out.println("a >= b is " + (a >= b));
        System.out.println("a <= b is " + (a <= b));
        System.out.println("a == c is " + (a == c));
        System.out.println("a != c is " + (a != c));
    }
}